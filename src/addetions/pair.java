public class pair  {
    public float first;
    public int second;

    public pair() {
    }

    public pair(float first, int second) {
        this.first = first;
        this.second = second;
    }

    public float getFirst() {
        return first;
    }

    public void setFirst(float first) {
        this.first = first;
    }

    public int getSecond() {
        return second;
    }

    public void setSecond(int second) {
        this.second = second;
    }

}
